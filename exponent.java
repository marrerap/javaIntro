public class exponent {
    public static void main(String[] args) {
        // pow takes two sets of numbers and raises the first value to the power of the second value
        System.out.println("2 raised to the power 3 is " + Math.pow(2, 3));
        // exp takes one argument and returns the value of e raised to the power of the given argument
        System.out.println("Exponent squared is " + Math.exp(2));
        // sqrt gives the square root of the given argument 
        System.out.println("The square root of 16 is " + Math.sqrt(16));
        // cbrt gives the cubed root of the given argument
        System.out.println("The cube root of 27 is " + Math.cbrt(27));
    }
}
