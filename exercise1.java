public class exercise1 {
    public static void main(String[] args) {
        int int_number = 1000;
        float float_number = 10.292f;
        double double_number = 0.00000000000512365123;
        char char_name = 'N';
        boolean bool_accept = true;

        System.out.println("Value of Int is: " + int_number);

        System.out.println("Value of Float is: " + float_number);

        System.out.println("Value of Double is: " + double_number);

        System.out.println("Value of Char is: " + char_name);

        System.out.println("Value of Bool is: " + bool_accept);
    }
}
